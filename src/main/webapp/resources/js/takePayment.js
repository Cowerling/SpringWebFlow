$(document).ready(function () {
    $("#creditCardNumber").hide();

    $("#cash").click(function (event) {
        $("#creditCardNumber").hide();
    });
    $("#cash").click(function (event) {
        $("#creditCardNumber").hide();
    });
    $("#creditCard").click(function (event) {
        $("#creditCardNumber").show();
    });
});